#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;


	i = 0;
	while (nb[i] != '\0')
	{
		if (nb[i] < 0)
		{
			write(1, "-", 1);
			i++;
		}	
		else if (nb[i] >= 0 && nb[i] <= 9)
		{
			write(1, nb[i + '0'], 1);
			i++;
		}
	}
}
